#Индекс участника с минимальным баллом
def indexMin(a):
    k = -1
    indMin = 0
    while k != min(a):
        k = a[indMin]
        indMin += 1
    return(indMin)

#Ввод парных сравнений
def parnSravn(a, b):
    k = 0
    for x in b:
        if x != min(b):
            ss = input("Введите оценку при сравнении с худшим результатом: ")
            a.append(int(ss))
        else:
            print("Значение равно 1")
            a.append(1)
            
