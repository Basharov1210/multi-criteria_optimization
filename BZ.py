def matrix(a, b, A):
    for i in range(7):
        A.append([0] * 7)

    for i in range(7):
        for j in range(7):
            if i == j:
                A[i][j] = 1.00
                continue

            if (j == indexMin(a) - 1) and (i != indexMin(a) - 1):
                A[i][j] = float("%.2f" % b[i])
                A[j][i] = "%.2f" % (1.00 / b[i])
    for i in range(7):
        for j in range(7):
            if (i == j) or (A[i][j] != 0):
                print(A[i][j], end = "      ")
                continue
            A[i][j] = "%.2f" % (float(A[indexMin(a)-1][j]) / float(A[indexMin(a)-1][i]))
            print(A[i][j], end = "      ")
        print()
    return(A)

def cMnog(A, c1, c2, c3, c4, c5, c6, c7):
    for i in range(7):
        c1 = c1 + float(A[i][0])
        c2 = c2 + float(A[i][1])
        c3 = c3 + float(A[i][2])
        c4 = c4 + float(A[i][3])
        c5 = c5 + float(A[i][4])
        c6 = c6 + float(A[i][5])
        c7 = c7 + float(A[i][6])
    return(float("%.2f" % (1 / c1)), float("%.2f" % (1 / c2)), float("%.2f" % (1 / c3)), float("%.2f" % (1 / c4)), float("%.2f" % (1 / c5)), float("%.2f" % (1 / c6)), float("%.2f" % (1 / c7)))


